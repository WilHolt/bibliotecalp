
#include "mbasic.h"


using namespace std;

namespace mbasic
{
    int MathBasic::add(int a, int b)
    {
        return a + b;
    }

    int MathBasic::dif(int a, int b)
    {
        return a - b;
    }

    int MathBasic::mux(int a, int b)
    {
        return a * b;
    }

    int MathBasic::div(int a, int b)
    {
        return a / b;
    }
}
